package tables;

public class Cadidato {
    private int idCandidato;
	private String nomeCandidato;
	private String partidoCadidato;
	private String fichaLimpaCandidato;
	
	public int getIdCandidato() {
		return idCandidato;
	}
	public void setIdCandidato(int idCandidato) {
		this.idCandidato = idCandidato;
	}
	public String getNomeCandidato() {
		return nomeCandidato;
	}
	public void setNomeCandidato(String nomeCandidato) {
		this.nomeCandidato = nomeCandidato;
	}
	public String getPartidoCadidato() {
		return partidoCadidato;
	}
	public void setPartidoCadidato(String partidoCadidato) {
		this.partidoCadidato = partidoCadidato;
	}
	public String getFichaLimpaCandidato() {
		return fichaLimpaCandidato;
	}
	public void setFichaLimpaCandidato(String fichaLimpaCandidato) {
		this.fichaLimpaCandidato = fichaLimpaCandidato;
	}
}
