package tables;

public class CandidatoPesquisa {
    private int idCandidatoPesquisa;
	private int votosCandidatoPesquisa;
	
	public int getIdCandidatoPesquisa() {
		return idCandidatoPesquisa;
	}
	public void setIdCandidatoPesquisa(int idCandidatoPesquisa) {
		this.idCandidatoPesquisa = idCandidatoPesquisa;
	}
	public int getVotosCandidatoPesquisa() {
		return votosCandidatoPesquisa;
	}
	public void setVotosCandidatoPesquisa(int votosCandidatoPesquisa) {
		this.votosCandidatoPesquisa = votosCandidatoPesquisa;
	}
}
